-- =========================
-- Tabla: temperaturas
-- =========================
create table if not exists public.temperaturas (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  valor_temp double precision not null,
  sensor_id text not null
);

create index if not exists idx_temperaturas_created_at
  on public.temperaturas (created_at asc);

create index if not exists idx_temperaturas_sensor_id
  on public.temperaturas (sensor_id);

alter table public.temperaturas enable row level security;

grant usage on schema public to anon, authenticated;
grant select on table public.temperaturas to anon, authenticated;
grant insert on table public.temperaturas to anon;
grant usage, select on sequence public.temperaturas_id_seq to anon;

drop policy if exists "anon can insert temperaturas" on public.temperaturas;
create policy "anon can insert temperaturas"
on public.temperaturas
for insert
to anon
with check (
  sensor_id in ('pecera_1', 'pecera_2', 'sensor-demo-001')
  and valor_temp between 0 and 60
  and created_at >= now() - interval '1 day'
  and created_at <= now() + interval '10 minutes'
);

drop policy if exists "public can read temperaturas" on public.temperaturas;
create policy "public can read temperaturas"
on public.temperaturas
for select
to anon, authenticated
using (true);

-- =========================
-- Tabla: comandos
-- =========================
create table if not exists public.comandos (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  accion text not null,
  sensor_id text not null,
  estado text not null default 'pendiente',
  constraint comandos_estado_check check (estado in ('pendiente', 'completado'))
);

create index if not exists idx_comandos_sensor_estado_created_at
  on public.comandos (sensor_id, estado, created_at desc);

alter table public.comandos enable row level security;

grant select, insert, update on table public.comandos to authenticated;
grant usage, select on sequence public.comandos_id_seq to authenticated;

drop policy if exists "authenticated can insert comandos" on public.comandos;
drop policy if exists "public can insert comandos" on public.comandos;
create policy "authenticated can insert comandos"
on public.comandos
for insert
to authenticated
with check (
  accion = 'forzar_lectura'
  and sensor_id in ('pecera_1', 'pecera_2')
  and estado = 'pendiente'
);

drop policy if exists "authenticated can read comandos" on public.comandos;
drop policy if exists "public can read comandos" on public.comandos;
create policy "authenticated can read comandos"
on public.comandos
for select
to authenticated
using (true);

drop policy if exists "authenticated can update comandos" on public.comandos;
drop policy if exists "public can update comandos" on public.comandos;
create policy "authenticated can update comandos"
on public.comandos
for update
to authenticated
using (sensor_id in ('pecera_1', 'pecera_2'))
with check (
  accion = 'forzar_lectura'
  and sensor_id in ('pecera_1', 'pecera_2')
  and estado in ('pendiente', 'completado')
);

